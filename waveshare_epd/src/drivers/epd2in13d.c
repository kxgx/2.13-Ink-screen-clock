#define CMD_PANEL_SETTING   0x00
#define CMD_POWER_SETTING   0x01
#define CMD_POWER_OFF       0x02
#define CMD_POWER_ON        0x04
#define CMD_BOOSTER_SOFT    0x06
#define CMD_DEEP_SLEEP      0x07
#define CMD_DATA_START_1    0x10
#define CMD_DISPLAY_REFRESH 0x12
#define CMD_DATA_START_2    0x13
#define CMD_LUT_VCOM        0x20
#define CMD_LUT_WW          0x21
#define CMD_LUT_BW          0x22
#define CMD_LUT_WB          0x23
#define CMD_LUT_BB          0x24
#define CMD_PLL_CONTROL     0x30
#define CMD_VCOM_DATA_INTRV 0x50
#define CMD_RESOLUTION      0x61
#define CMD_GET_STATUS      0x71
#define CMD_VCM_DC_SETTING  0x82
#define CMD_PARTIAL_IN      0x91

static void _busy(EPD *epd) {
    while (epd->digital_read(epd->busy_pin) == 0) {
        epd_send_command(epd, CMD_GET_STATUS);
        epd->delay_ms(100);
    }
}

static void _set_full_reg(EPD *epd) {
    epd_send_command(epd, CMD_VCM_DC_SETTING); epd_send_data(epd, 0x00);
    epd_send_command(epd, CMD_VCOM_DATA_INTRV); epd_send_data(epd, 0x97);
    epd_send_command(epd, CMD_LUT_VCOM); epd_send_data_buf(epd, lut_vcomDC, 44);
    epd_send_command(epd, CMD_LUT_WW);   epd_send_data_buf(epd, lut_ww, 42);
    epd_send_command(epd, CMD_LUT_BW);   epd_send_data_buf(epd, lut_bw, 42);
    epd_send_command(epd, CMD_LUT_WB);   epd_send_data_buf(epd, lut_wb, 42);
    epd_send_command(epd, CMD_LUT_BB);   epd_send_data_buf(epd, lut_bb, 42);
}

static void _set_part_reg(EPD *epd) {
    epd_send_command(epd, CMD_VCM_DC_SETTING); epd_send_data(epd, 0x03);
    epd_send_command(epd, CMD_VCOM_DATA_INTRV); epd_send_data(epd, 0x47);
    epd_send_command(epd, CMD_LUT_VCOM); epd_send_data_buf(epd, lut_vcom1, 44);
    epd_send_command(epd, CMD_LUT_WW);   epd_send_data_buf(epd, lut_ww1, 42);
    epd_send_command(epd, CMD_LUT_BW);   epd_send_data_buf(epd, lut_bw1, 42);
    epd_send_command(epd, CMD_LUT_WB);   epd_send_data_buf(epd, lut_wb1, 42);
    epd_send_command(epd, CMD_LUT_BB);   epd_send_data_buf(epd, lut_bb1, 42);
}

int EPD_2in13d_Init(EPD *epd) {
    if (!epd) return EPD_ERROR;
    epd->width = EPD2IN13D_WIDTH; epd->height = EPD2IN13D_HEIGHT;
    epd->rst_pin = EPD_RST_PIN; epd->dc_pin = EPD_DC_PIN;
    epd->cs_pin = EPD_CS_PIN; epd->busy_pin = EPD_BUSY_PIN; epd->pwr_pin = EPD_PWR_PIN;
    if (epd_setup_default_hal(epd) != EPD_OK) return EPD_ERROR;

    epd_reset(epd);

    epd_send_command(epd, CMD_POWER_SETTING);
    epd_send_data(epd, 0x03); epd_send_data(epd, 0x00); epd_send_data(epd, 0x2B); epd_send_data(epd, 0x2B); epd_send_data(epd, 0x03);

    epd_send_command(epd, CMD_BOOSTER_SOFT);
    epd_send_data(epd, 0x17); epd_send_data(epd, 0x17); epd_send_data(epd, 0x17);

    epd_send_command(epd, CMD_POWER_ON);
    _busy(epd);

    epd_send_command(epd, CMD_PANEL_SETTING);
    epd_send_data(epd, 0xBF); epd_send_data(epd, 0x0D);

    epd_send_command(epd, CMD_PLL_CONTROL);
    epd_send_data(epd, 0x3A);

    epd_send_command(epd, CMD_RESOLUTION);
    epd_send_data(epd, epd->width & 0xFF);
    epd_send_data(epd, (epd->height >> 8) & 0xFF);
    epd_send_data(epd, epd->height & 0xFF);

    epd_send_command(epd, CMD_VCM_DC_SETTING);
    epd_send_data(epd, 0x28);
    return EPD_OK;
}

void EPD_2in13d_Display(EPD *epd, const uint8_t *image) {
    if (!epd || !image) return;
    int sz = epd_linewidth(epd->width) * epd->height;

    epd_send_command(epd, CMD_DATA_START_1);
    /* Send old data (white) to data_start_1 */
    uint8_t *white = (uint8_t *)calloc(1, sz);
    if (white) {
        epd_send_data_buf(epd, white, sz);
        free(white);
    }
    epd->delay_ms(10);

    epd_send_command(epd, CMD_DATA_START_2);
    epd_send_data_buf(epd, image, sz);
    epd->delay_ms(10);

    _set_full_reg(epd);
    epd_send_command(epd, CMD_DISPLAY_REFRESH);
    epd->delay_ms(100);
    _busy(epd);
}

void EPD_2in13d_DisplayPartial(EPD *epd, const uint8_t *image) {
    if (!epd || !image) return;
    int lw = epd_linewidth(epd->width);
    int sz = epd->height * lw;

    epd_send_command(epd, CMD_PARTIAL_IN);
    epd_send_command(epd, 0x90);
    epd_send_data(epd, 0);
    epd_send_data(epd, epd->width - 1);
    epd_send_data(epd, 0);
    epd_send_data(epd, 0);
    epd_send_data(epd, epd->height >> 8);
    epd_send_data(epd, (epd->height - 1) & 0xFF);
    epd_send_data(epd, 0x28);

    uint8_t *buf = (uint8_t *)malloc(sz);
    if (!buf) return;
    for (int i = 0; i < sz; i++) buf[i] = ~image[i];

    epd_send_command(epd, CMD_DATA_START_1);
    epd_send_data_buf(epd, image, sz);
    epd->delay_ms(10);

    epd_send_command(epd, CMD_DATA_START_2);
    epd_send_data_buf(epd, buf, sz);
    epd->delay_ms(10);
    free(buf);

    _set_part_reg(epd);
    epd_send_command(epd, CMD_DISPLAY_REFRESH);
    epd->delay_ms(100);
    _busy(epd);
}

void EPD_2in13d_Clear(EPD *epd) {
    if (!epd) return;
    int sz = epd_linewidth(epd->width) * epd->height;

    uint8_t *zero_buf = (uint8_t *)calloc(1, sz);
    if (!zero_buf) return;
    epd_send_command(epd, CMD_DATA_START_1);
    epd_send_data_buf(epd, zero_buf, sz);
    free(zero_buf);
    epd->delay_ms(10);

    uint8_t *white = (uint8_t *)malloc(sz);
    if (!white) return;
    memset(white, 0xFF, sz);
    epd_send_command(epd, CMD_DATA_START_2);
    epd_send_data_buf(epd, white, sz);
    epd->delay_ms(10);
    free(white);

    _set_full_reg(epd);
    epd_send_command(epd, CMD_DISPLAY_REFRESH);
    epd->delay_ms(100);
    _busy(epd);
}

void EPD_2in13d_Sleep(EPD *epd) {
    if (!epd) return;
    epd_send_command(epd, CMD_VCOM_DATA_INTRV); epd_send_data(epd, 0xF7);
    epd_send_command(epd, CMD_POWER_OFF);
    epd_send_command(epd, CMD_DEEP_SLEEP); epd_send_data(epd, 0xA5);
    epd->delay_ms(2000);
    epd_teardown_default_hal(epd);
}
